package dependencies

import (
	"fmt"
	"os"
	"path/filepath"
	"time"

	"gopkg.in/yaml.v3"
)

// Learner handles learning and remembering successful dependency configurations
type Learner struct {
	memoryDir string
	loader    *Loader
}

// NewLearner creates a new learner instance
func NewLearner(memoryDir string, loader *Loader) *Learner {
	if memoryDir == "" {
		memoryDir = ".installer/memory/dependencies"
	}
	return &Learner{
		memoryDir: memoryDir,
		loader:    loader,
	}
}

// LearnFromSuccess records a successful installation for future reference
func (l *Learner) LearnFromSuccess(node *DependencyNode, installMethod *InstallMethod) error {
	if node == nil || node.ID == "" {
		return fmt.Errorf("invalid dependency node")
	}

	// Create memory entry
	entry := &MemoryEntry{
		ID:        node.ID,
		LearnedAt: time.Now(),
		Source:    "install_success",
		Dependency: DependencyNode{
			ID:              node.ID,
			Name:            node.Name,
			Description:     node.Description,
			Requirements:    node.Requirements,
			Dependencies:    node.Dependencies,
			Platforms:       node.Platforms,
			Provides:        node.Provides,
			InstallMethods:  []InstallMethod{},
			VersionRequirements: node.VersionRequirements,
		},
		SuccessCount: 1,
		LastUsed:     time.Now(),
	}

	// Add the successful install method if provided
	if installMethod != nil {
		entry.Dependency.InstallMethods = []InstallMethod{*installMethod}
	}

	// Check if entry already exists
	existingPath := filepath.Join(l.memoryDir, node.ID+".yaml")
	if data, err := os.ReadFile(existingPath); err == nil {
		var existing MemoryEntry
		if err := yaml.Unmarshal(data, &existing); err == nil {
			// Update existing entry
			entry.SuccessCount = existing.SuccessCount + 1
			entry.Source = existing.Source

			// Merge install methods
			if installMethod != nil {
				methods := existing.Dependency.InstallMethods
				found := false
				for i, m := range methods {
					if m.Type == installMethod.Type {
						// Update existing method with successful one
						methods[i] = *installMethod
						found = true
						break
					}
				}
				if !found {
					methods = append(methods, *installMethod)
				}
				entry.Dependency.InstallMethods = methods
			} else {
				entry.Dependency.InstallMethods = existing.Dependency.InstallMethods
			}

			// Keep other fields from existing if not in new
			if entry.Dependency.Name == "" {
				entry.Dependency.Name = existing.Dependency.Name
			}
			if entry.Dependency.Description == "" {
				entry.Dependency.Description = existing.Dependency.Description
			}
		}
	}

	// Ensure directory exists
	if err := os.MkdirAll(l.memoryDir, 0755); err != nil {
		return fmt.Errorf("failed to create memory directory: %w", err)
	}

	// Write entry
	data, err := yaml.Marshal(entry)
	if err != nil {
		return fmt.Errorf("failed to marshal memory entry: %w", err)
	}

	filename := filepath.Join(l.memoryDir, node.ID+".yaml")
	if err := os.WriteFile(filename, data, 0644); err != nil {
		return fmt.Errorf("failed to write memory file: %w", err)
	}

	return nil
}

